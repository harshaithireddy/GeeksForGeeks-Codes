class Solution {
  public:
    vector<int> subarraySum(vector<int> &arr, int target) {
        // code here
        int i = 0, j = 0;
        int n = arr.size();
        long long sum = 0;
        while(i < n) {
            sum += arr[i];
            while(j < n && sum > target) {
                sum -= arr[j];
                j++;
            }
            if(i >= j && sum == target) return {j + 1 , i + 1};
            i++;
        }
        return {-1};
    }
};

